#!/usr/bin/env node

import { mkdir, writeFile } from "node:fs/promises";
import { join } from "node:path";

import { defaultPublicPreviewChecks, runPublicPreviewHealthCheck } from "./public-preview-health-check.mjs";

export const defaultRenderHealthStatusUrl = "https://finance-ai-assistant-web.onrender.com";
export const defaultRenderHealthStatusOutputDir = ".render-health-status";

function parseNumber(value, fallback) {
  const parsed = Number(value);
  return Number.isFinite(parsed) && parsed > 0 ? parsed : fallback;
}

function parseCliArgs(argv = process.argv.slice(2), env = process.env) {
  const args = new Map();
  for (let index = 0; index < argv.length; index += 1) {
    const item = argv[index];
    if (!item.startsWith("--")) continue;
    const [key, inlineValue] = item.slice(2).split("=");
    const value = inlineValue !== undefined ? inlineValue : argv[index + 1];
    args.set(key, value);
    if (inlineValue === undefined) index += 1;
  }
  return {
    url: args.get("url") || env.RENDER_URL || env.FINANCE_AI_STABLE_PREVIEW_URL || defaultRenderHealthStatusUrl,
    outputDir: args.get("output-dir") || env.RENDER_HEALTH_OUTPUT_DIR || defaultRenderHealthStatusOutputDir,
    durationMs: parseNumber(args.get("duration-ms") || env.RENDER_HEALTH_DURATION_MS, 180_000),
    intervalMs: parseNumber(args.get("interval-ms") || env.RENDER_HEALTH_INTERVAL_MS, 15_000),
    timeoutMs: parseNumber(args.get("timeout-ms") || env.RENDER_HEALTH_TIMEOUT_MS, 30_000),
  };
}

function escapeHtml(value = "") {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#39;");
}

function toRenderHealthStatus(health) {
  const updatedAt = health.endedAt || new Date().toISOString();
  const status = health.ok ? "healthy" : "failed";
  const iterations = Array.isArray(health.iterations) ? health.iterations : [];
  const endpointResults = iterations.flatMap((iteration) =>
    Array.isArray(iteration.results) ? iteration.results : [],
  );
  const healthSuccessCount = iterations.filter((iteration) => iteration.ok === true).length;
  const healthFailureCount = iterations.filter((iteration) => iteration.ok !== true).length;
  const endpointSuccessCount = endpointResults.filter((result) => result.ok === true).length;
  const endpointFailureCount = endpointResults.filter((result) => result.ok !== true).length;
  return {
    service: "finance-ai-assistant-web",
    url: health.publicUrl,
    publicUrl: health.publicUrl,
    ok: health.ok,
    status,
    updatedAt,
    startedAt: health.startedAt,
    endedAt: health.endedAt,
    healthStartedAt: health.startedAt,
    healthEndedAt: health.endedAt,
    healthWindowMs: health.durationMs,
    healthIntervalMs: health.intervalMs,
    healthTimeoutMs: health.timeoutMs,
    healthRequiredEndpoints: health.checkedEndpoints,
    healthIterationCount: health.iterationCount,
    healthSuccessCount,
    healthFailureCount,
    endpointSuccessCount,
    endpointFailureCount,
    transientFailureCount: health.transientFailureCount,
    lastFailure: health.lastFailure,
    localFallbackOk: health.localFallback?.ok ?? null,
    checks: iterations.at(-1)?.results || [],
    iterations,
    stabilityGate: {
      externalUseReady: health.ok,
      continuousHealthPassed: health.ok,
      requiredDurationSeconds: Math.round(health.durationMs / 1000),
      monitorWindowSeconds: Math.round(health.durationMs / 1000),
      monitorIterationCount: health.iterationCount,
      requiredEndpointCoverage: defaultPublicPreviewChecks.every((check) =>
        health.checkedEndpoints.includes(check.path),
      ),
      lastFailureType: health.lastFailure?.failureType || "",
    },
    note:
      "Generated by GitHub Actions or a local script to keep the free Render service warm and expose the latest 2-3 minute stable access gate. No secrets are written.",
    guidance: health.guidance,
  };
}

function renderStatusHtml(status) {
  const checkRows = (Array.isArray(status.checks) ? status.checks : [])
    .map(
      (check) => `
        <tr>
          <td>${escapeHtml(check.path || check.label || check.id || "")}</td>
          <td class="${check.ok ? "ok" : "bad"}">${escapeHtml(check.ok ? "200 / ok" : String(check.status || "failed"))}</td>
          <td>${escapeHtml(`${check.durationMs || 0}ms`)}</td>
        </tr>
      `,
    )
    .join("");
  return `<!doctype html>
<html lang="zh-CN">
  <head>
    <meta charset="utf-8" />
    <meta name="viewport" content="width=device-width, initial-scale=1" />
    <title>Finance AI Render Health</title>
    <style>
      body { font-family: -apple-system, BlinkMacSystemFont, "Segoe UI", sans-serif; margin: 40px; color: #17211d; background: #f6faf7; }
      main { max-width: 860px; margin: 0 auto; }
      code, pre { background: #e8f3ed; border-radius: 6px; padding: 2px 6px; }
      table { width: 100%; border-collapse: collapse; margin-top: 18px; background: white; }
      th, td { border-bottom: 1px solid #dbe8df; padding: 10px; text-align: left; }
      .ok { color: #067647; font-weight: 700; }
      .bad { color: #b42318; font-weight: 700; }
    </style>
  </head>
  <body>
    <main>
      <h1>Finance AI Render Health</h1>
      <p>Status: <span class="${status.ok ? "ok" : "bad"}">${escapeHtml(status.ok ? "passed" : "failed")}</span></p>
      <p>Checked at: <code>${escapeHtml(status.updatedAt)}</code></p>
      <p>Target: <a href="${escapeHtml(status.url)}">${escapeHtml(status.url)}</a></p>
      <p>Window: <code>${escapeHtml(String(Math.round(status.healthWindowMs / 1000)))}s</code> · Iterations: <code>${escapeHtml(String(status.healthIterationCount))}</code></p>
      <p>Script checks: <code>${escapeHtml(String(status.healthSuccessCount || 0))}</code> success / <code>${escapeHtml(String(status.healthFailureCount || 0))}</code> failure · Endpoint checks: <code>${escapeHtml(String(status.endpointSuccessCount || 0))}</code> success / <code>${escapeHtml(String(status.endpointFailureCount || 0))}</code> failure</p>
      <p>JSON: <a href="./render-health.json">render-health.json</a></p>
      <table>
        <thead><tr><th>Endpoint</th><th>Status</th><th>Duration</th></tr></thead>
        <tbody>${checkRows}</tbody>
      </table>
      <p>中文：这个页面由 GitHub Actions 免费定时任务或本地脚本生成，用来预热 Render 并记录稳定访问门禁。</p>
      <p>English: This page is generated by a free GitHub Actions schedule or local script to warm Render and record the stable access gate.</p>
    </main>
  </body>
</html>
`;
}

export async function runRenderHealthStatusCheck(options = {}) {
  const health = await runPublicPreviewHealthCheck({
    publicUrl: options.url || defaultRenderHealthStatusUrl,
    durationMs: options.durationMs || 180_000,
    intervalMs: options.intervalMs || 15_000,
    timeoutMs: options.timeoutMs || 30_000,
    localFallbackUrl: options.localFallbackUrl || "http://127.0.0.1:4192",
    checks: options.checks || defaultPublicPreviewChecks,
    fetchImpl: options.fetchImpl,
    sleep: options.sleep,
    now: options.now,
    allowCurlFallback: options.allowCurlFallback,
    execFileImpl: options.execFileImpl,
  });
  const status = toRenderHealthStatus(health);
  const outputDir = options.outputDir || defaultRenderHealthStatusOutputDir;
  await mkdir(outputDir, { recursive: true });
  const jsonPath = join(outputDir, "render-health.json");
  const htmlPath = join(outputDir, "index.html");
  await writeFile(jsonPath, `${JSON.stringify(status, null, 2)}\n`);
  await writeFile(htmlPath, renderStatusHtml(status));
  return { status, jsonPath, htmlPath };
}

if (import.meta.url === `file://${process.argv[1]}`) {
  try {
    const result = await runRenderHealthStatusCheck(parseCliArgs());
    console.log(JSON.stringify({ ok: result.status.ok, jsonPath: result.jsonPath, htmlPath: result.htmlPath }, null, 2));
    process.exitCode = result.status.ok ? 0 : 1;
  } catch (error) {
    console.error(error?.message || error);
    process.exitCode = 2;
  }
}
